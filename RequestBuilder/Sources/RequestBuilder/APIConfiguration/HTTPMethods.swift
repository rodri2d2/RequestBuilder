import Foundation


/// Enume defining the most common HTTP methods used in API calls.
///
/// Each instance of `HTTPMethod` represents a specific HTTP method.
///
public enum HTTPMethod: String {
    case GET
    case POST
    case PUT
    case DELETE
}
